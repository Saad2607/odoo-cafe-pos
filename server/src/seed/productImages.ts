const KEYWORD_ORDER = Object.keys(KEYWORD_POOL).sort((a, b) => b.length - a.length);

function hashString(s: string): number {
  let h = 0;
  for (let i = 0; i < s.length; i++) h = ((h << 5) - h + s.charCodeAt(i)) | 0;
  return Math.abs(h);
}

function pickFromPool(pool: string[], name: string, salt = ''): string {
  const idx = (hashString(name + salt) + hashString(name.split('').reverse().join(''))) % pool.length;
  return pool[idx];
}

function matchKeyword(name: string): string | null {
  const lower = name.toLowerCase();
  for (const kw of KEYWORD_ORDER) {
    if (lower.includes(kw)) return kw;
  }
  return null;
}

export function resolveProductImageUrl(name: string, category?: string): string {
  if (EXACT[name]) return EXACT[name];

  const kw = matchKeyword(name);
  if (kw) return pickFromPool(KEYWORD_POOL[kw], name, kw);

  if (category && CATEGORY_POOL[category]) {
    return pickFromPool(CATEGORY_POOL[category], name, category);
  }

  return pickFromPool(GLOBAL_POOL, name, 'global');
}

export function resolveProductImageAlt(name: string, category?: string): string {
  if (EXACT[name]) return EXACT[name];
  const kw = matchKeyword(name);
  if (kw) return pickFromPool(KEYWORD_POOL[kw], name, kw + '-alt');
  if (category && CATEGORY_POOL[category]) {
    return pickFromPool(CATEGORY_POOL[category], name, category + '-alt');
  }
  return pickFromPool(GLOBAL_POOL, name, 'global-alt');
}

export function assignImagesToCatalog<T extends { name: string; category: string; imageUrl?: string }>(
  items: T[],
): (T & { imageUrl: string })[] {
  return items.map((item) => ({
    ...item,
    imageUrl: resolveProductImageUrl(item.name, item.category),
  }));
}
